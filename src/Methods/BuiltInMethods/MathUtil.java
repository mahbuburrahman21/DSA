package Methods.BuiltInMethods;

public class MathUtil {
    static void main(String[] args) {

        System.out.println(Math.max(Math.max(10,30),Math.max(20,40)));
        System.out.println(Math.pow(2,3));
        System.out.println(Math.pow(-2,3));
        System.out.println(Math.pow(-2,-3));
        System.out.println(Math.pow(-2,-3.3));
        System.out.println(Math.pow(0,0));
        System.out.println(Math.min(2,3));
        System.out.println(Math.floor(2.87));
        System.out.println(Math.floor(-2.87));
        System.out.println(Math.ceil(2.837));
        System.out.println(Math.ceil(-2.837));
        System.out.println(Math.sqrt(16));
        System.out.println(Math.cbrt(125));

    }
}
